CREATE VIEW V_PERSON_MAIN AS
SELECT PR.ID [User Name ],PR.EMAIL [E-Mail],PR.FIRST_NAME [First Name],PR.LAST_NAME [Last Name ],
  PR.STREET [Street],PR.APT [Apartment],PR.CITY [City],ST.DESCRIPTION [State],PR.ZIP [Zip],
  '('+SUBSTRING (PR.HOME_PHONE,1,3)+') '+ SUBSTRING (PR.HOME_PHONE,4,3)+'-'+SUBSTRING (PR.HOME_PHONE,7,3) [Home Phone],
	PR.WORK_PHONE [Work Phone],PR.CELL_PHONE [Cell Phone],
  PR.SEX [Gender],PR.BIRTHDATE [Birthdate],PR.REGISTRATION_DATE [Registration Date],
  PR.PUBLISH [Shown]
FROM PERSON PR 
LEFT JOIN  STATE ST ON PR.STATE=ST.CODE
