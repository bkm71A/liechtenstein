CREATE VIEW V_PERSON_EXTRA AS
SELECT PE.ID [User Name],PE.CASUAL,PE.LONGTERM,PE.MARRIAGE,PE.RELOCATE [Willing to relocate?],
CW.DESCRIPTION [Children if married?],AR.SHORT [Astrological Sign],COB.DESCRIPTION [Country of birth],
COG.DESCRIPTION [Grownup Country],MA.DESCRIPTION [Marital Status],PE.SINGLE_SINCE [Single Since],
CH.DESCRIPTION [Children],PE.NUM_CHILDREN [Number of children(if any)],PE.CHILD_AGES [Children Ages],
ET.DESCRIPTION [Ethnicity],RE.DESCRIPTION [Religion], SY.DESCRIPTION [Synagogue],KO.DESCRIPTION [Keeps Kosher],
PE.HEIGHT_INCHES [Height in inches],PE.WEIGHT [Weight in pounds],BT.DESCRIPTION [Body Type],HC.DESCRIPTION [Hair Color],
EC.DESCRIPTION [Eye Color],DK.DESCRIPTION [Drink],SK.DESCRIPTION [Smoke],
HE.DESCRIPTION [Health],CL.DESCRIPTION [Occupation],IC.DESCRIPTION [Income],
DG.DESCRIPTION [Degree],PE.STUDIES [Emphasis of Studies],PE.SCHOOL [School],
PL.DESCRIPTION [Political],PA.DESCRIPTION [Physical Activity],PE.FAVORITE_SPORT [Favorite Sport],
PE.FAVORITE_HOBBY [Favorite Hobby],PE.FAVORITE_VACATION [Favorite Vacation],PE.STATEMENT [Brief Statement]
FROM PERSON_EXTRA PE 
LEFT JOIN  CHILDREN_WILL CW ON PE.CHILDREN_WILL=CW.CODE
LEFT JOIN  ASTROLOGICAL AR ON PE.ASTROLOGICAL=AR.CODE
LEFT JOIN  COUNTRY COB ON PE.BIRTH_COUNTRY=COB.CODE
LEFT JOIN  COUNTRY COG ON PE.GREWUP_COUNTRY=COG.CODE
LEFT JOIN  MARITAL MA ON PE.MARITAL_STATUS=MA.CODE
LEFT JOIN  CHILDREN CH ON PE.CHILDREN=CH.CODE
LEFT JOIN  ETHNICITY ET ON PE.ETHNICITY=ET.CODE
LEFT JOIN  RELIGION RE ON PE.RELIGION=RE.CODE
LEFT JOIN  SYNAGOGUE SY ON PE.SYNAGOGUE=SY.CODE
LEFT JOIN  KOSHER KO ON PE.KOSHER=KO.CODE
LEFT JOIN  BODY_TYPE BT ON PE.BODY_TYPE=BT.CODE
LEFT JOIN  HAIR_COLOR HC ON PE.HAIR=HC.CODE
LEFT JOIN  EYE_COLOR EC ON PE.EYE=EC.CODE
LEFT JOIN  DRINK DK ON PE.DRINK=DK.CODE
LEFT JOIN  SMOKE SK ON PE.SMOKE=SK.CODE
LEFT JOIN  HEALTH HE ON PE.HEALTH=HE.CODE
LEFT JOIN  CHECKBOX_LIST CL ON PE.OCCUPATION=CL.CODE
LEFT JOIN  INCOME IC ON PE.INCOME=IC.CODE
LEFT JOIN  DEGREE DG ON PE.DEGREE=DG.CODE
LEFT JOIN  POLITICAL PL ON PE.POLITICAL=PL.CODE
LEFT JOIN  PHYSICAL_ACTIVITY PA ON PE.ACTIVITY=PA.CODE